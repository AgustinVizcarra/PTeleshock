package pe.edu.pucp.pteleshock.Beans;

public class Bbloqueadas {
    private String administrador_nombre;
    private String farmacia_nombre;
    private String razon_motivo;
    private String fecha;

    public String getAdministrador_nombre() {
        return administrador_nombre;
    }

    public void setAdministrador_nombre(String administrador_nombre) {
        this.administrador_nombre = administrador_nombre;
    }

    public String getFarmacia_nombre() {
        return farmacia_nombre;
    }

    public void setFarmacia_nombre(String farmacia_nombre) {
        this.farmacia_nombre = farmacia_nombre;
    }

    public String getRazon_motivo() {
        return razon_motivo;
    }

    public void setRazon_motivo(String razon_motivo) {
        this.razon_motivo = razon_motivo;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }
}

